void vGL_reverseArea(unsigned int x0, unsigned int y0, unsigned int x1, unsigned int y1) {
    if ((x0 >= VIR_LCD_PIX_W)) {
        x0 = VIR_LCD_PIX_W - 1;
    }
    if ((y0 >= VIR_LCD_PIX_H)) {
        y0 = VIR_LCD_PIX_H - 1;
    }
    if ((x1 >= VIR_LCD_PIX_W)) {
        x1 = VIR_LCD_PIX_W - 1;
    }
    if ((y1 >= VIR_LCD_PIX_H)) {
        y1 = VIR_LCD_PIX_H - 1;
    }
    for (int y = y0; y < y1; y++){
      for (int x = x0; x < x1; x++) {
        ti8bpp_screen[x + y * VIR_LCD_PIX_W] = ~ti8bpp_screen[x + y * VIR_LCD_PIX_W];
      }
    }
}


