void vGL_set_pixel(unsigned x,unsigned y,int c){
  if (x>=VIR_LCD_PIX_W || y>=VIR_LCD_PIX_H)
    return;
  ti8bpp_screen[x + y * VIR_LCD_PIX_W] = c;
}

int vGL_get_pixel(unsigned int x,unsigned int y){
  if (x >= VIR_LCD_PIX_W || y >= VIR_LCD_PIX_H) 
    return -1;
  return ti8bpp_screen[x + y * VIR_LCD_PIX_W];
}  

inline int my_min(int a,int b){
  return a<b?a:b;
}

void vGL_putChar(unsigned int x0, unsigned int y0, char ch, unsigned char fg, unsigned char bg, unsigned char * charptrbase,int font_w,int font_h) {
  if (x0>=VIR_LCD_PIX_W || y0>=VIR_LCD_PIX_H)
    return;
  if ((ch < ' ') || (ch > '~' + 1)) {
    return;
  }
  if (ch==' '){
    vGL_setArea(x0,y0,x0+font_w,y0+font_h,bg);
    return;
  }
  const unsigned char * pCh = charptrbase + (ch - ' ') * font_h;
  int shift=my_min(font_w,VIR_LCD_PIX_W-x0); // shift>0
  unsigned char * ptry=ti8bpp_screen+x0+(y0<<8)+(y0<<6);
  int tmp=VIR_LCD_PIX_H-y0;
  if (font_h<tmp)
    tmp=font_h;
  unsigned char * ptryend=ptry+(tmp<<8)+(tmp<<6);
  for (;ptry<ptryend;ptry+=VIR_LCD_PIX_W,++pCh){
    unsigned char * ptr=ptry;
    unsigned char * ptrend = ptr+shift;
    unsigned char val=*pCh;
    if (val==0){
      for (;ptr<ptrend;++ptr)
        *ptr=bg;
    }
    else {
#if 1 // loop unroll, at most 8 iterations (font_w<=8)
      *ptr=(val & 0x80U)?fg:bg;
      ++ptr;
      if (ptr==ptrend) continue;
      *ptr=(val & 0x40U)?fg:bg;
      ++ptr;
      if (ptr==ptrend) continue;
      *ptr=(val & 0x20U)?fg:bg;
      ++ptr;
      if (ptr==ptrend) continue;
      *ptr=(val & 0x10U)?fg:bg;
      ++ptr;
      if (ptr==ptrend) continue;
      *ptr=(val & 0x8U)?fg:bg;
      ++ptr;
      if (ptr==ptrend) continue;
      *ptr=(val & 0x4U)?fg:bg;
      ++ptr;
      if (ptr==ptrend) continue;
      *ptr=(val & 0x2U)?fg:bg;
      ++ptr;
      if (ptr==ptrend) continue;
      *ptr=(val & 0x1U)?fg:bg;
#else
      for (;ptr<ptrend;++ptr){
        *ptr=(val & 0x80U)?fg:bg;
        val <<= 1;
      }
#endif
    }
  }
}

void vGL_putString(int x0, int y0, const char *s, unsigned char fg, unsigned char bg, int fontSize) {
  //dbg_printf("putstring x=%i y=%i s=%s fontSize=%i fg=%i bg=%i\n",x0,y0,s,fontSize,fg,bg); 
  int font_w;
  int font_h;
  int len = strlen(s);
  int x = 0, y = 0;
  unsigned char * charptrbase=VGA_Ascii_7x14;
  if (fontSize <= 16) {
    switch (fontSize) {
    case 8:
      font_w = 5;
      font_h = 8;
      charptrbase= VGA_Ascii_5x8;
      break;
      /*
    case 12:
      font_w = 8;
      font_h = 12;
      break;
      */
    case 14:
      font_w = 7;
      font_h = 14;
      break;
    case 16:
      font_w = 8;
      font_h = 16;
      charptrbase=VGA_Ascii_8x16;
      break;
    default:
      font_w = 7;
      font_h = 14;
      break;
    }
    
    while (*s) {
      vGL_putChar(x0 + x, y0 + y, *s, fg, bg, charptrbase,font_w,font_h);
      s++;
      x += font_w;
      if (x > VIR_LCD_PIX_W) {
        break; // otherwise goes next line?
        x = 0;
        y += font_h;
        if (y > VIR_LCD_PIX_H) {
          break;
        }
      }
    }
  }
  
}

